import math

"""
radians(x) 将角度转换为弧度。
"""

print("radians(90) : ", math.radians(90))  # 1 弧度等于大概 57.3°
print("radians(45) : ", math.radians(45))
print("radians(30) : ", math.radians(30))
print("radians(180) : ", math.radians(180))  # 180 度的弧度为 π

print("180 / pi : ", end="")
print(math.radians(180 / math.pi))
