"""
len() 方法返回对象（字符、列表、元组等）长度或项目个数。
"""

str1 = 'ADSFhoij'
print("str1：", len(str1))

list1 = [1, 2, 45, 1]
print("list1：", len(list1))

tuple1 = (1, 2, 5, 7, 5)
print("tuple1：", len(tuple1))

set1 = {"dasd", 1, 55, 12, 45}
print("set1：", len(set1))

dict1 = {'age': 18, 'name': "张三"}
print("dict1：", len(dict1))
