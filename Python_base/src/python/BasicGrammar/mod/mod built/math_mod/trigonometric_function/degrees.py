import math

"""
degrees(x) 将弧度转换为角度。
"""

print("degrees(3) : ", math.degrees(3))
print("degrees(-3) : ", math.degrees(-3))
print("degrees(0) : ", math.degrees(0))
print("degrees(math.pi) : ", math.degrees(math.pi))
print("degrees(math.pi/2) : ", math.degrees(math.pi / 2))
print("degrees(math.pi/4) : ", math.degrees(math.pi / 4))
